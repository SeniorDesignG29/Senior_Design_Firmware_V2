void setup() {
    Serial.begin(115200);
    Wire.begin();

    pinMode(PUMP_PIN, OUTPUT);
    digitalWrite(PUMP_PIN, LOW);
    pinMode(IR_SENSOR_PIN, INPUT); // pull-up is external 10kΩ to 3.3V. vcc wire connects to 3.3V, gnd wire to GND, signal wire to this pin, and resistor between signal and 3.3V.

    stepper.setMaxSpeed(800);
    stepper.setAcceleration(400);

    rtc.begin();

    // FIX: display correctly initialized as 128x128
    display.begin(I2C_ADDRESS, true);
    display.setRotation(0);

    // Show logo — FIX: drawBitmap now uses correct 128x128 dimensions
    display.clearDisplay();
    display.drawBitmap(0, 0, logo_bmp, 128, 128, SH110X_WHITE);
    display.display();
    delay(2000);

    WiFi.begin(ssid, password);
    while (WiFi.status() != WL_CONNECTED) delay(500);
    Serial.println("WiFi connected: " + WiFi.localIP().toString());

    server.on("/takeNow",    HTTP_GET,  handleTakeNow);
    server.on("/api/update", HTTP_POST, handleUpdate);
    server.begin();

    updateDisplay();
}

void loop() {
    server.handleClient();
    DateTime now = rtc.now();

    // Sanity check — if RTC returns year 2000 it was never synced
    static bool rtcSyncWarned = false;
    if (!rtcSyncWarned && now.year() < 2024) {
        Serial.println("!!! RTC NOT SYNCED — Open the app to sync time. Year shows: " + String(now.year()));
        rtcSyncWarned = true;
    }

    static bool dispatchedThisMinute = false;
    if (timerActive) {
        if (now.hour() == targetHour && now.minute() == targetMinute) {
            if (!dispatchedThisMinute) {
                dispatchedThisMinute = true;
                Serial.println(">>> Scheduled dispense triggered!");
                sendPushoverNotification(
                    "⚠️ Missed Medication",
                    userName + " missed their scheduled " + medName +
                    " at " + medTime + ". The dispenser has automatically released the medication."
                );
                runDispenseSequence();
                detectPill(); // log pill detection after scheduled dispense
            }
        } else {
            dispatchedThisMinute = false;
        }
    }

    static unsigned long lastUpdate = 0;
    if (millis() - lastUpdate > 5000) {
        DateTime now2 = rtc.now();
        Serial.printf("RTC: %02d:%02d:%02d | Target: %02d:%02d | Active: %s | Match: %s\n",
            now2.hour(), now2.minute(), now2.second(),
            targetHour, targetMinute,
            timerActive ? "YES" : "NO",
            (now2.hour() == targetHour && now2.minute() == targetMinute) ? "YES !!!" : "no"
        );
        updateDisplay();
        lastUpdate = millis();
    }
}
