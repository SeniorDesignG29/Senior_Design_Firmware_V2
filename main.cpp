// ─── Helpers ──────────────────────────────────────────────────────────────────

String extractJsonString(const String& body, const String& key) {
    String searchKey = "\"" + key + "\":\"";
    int idx = body.indexOf(searchKey);
    if (idx == -1) return "";
    int start = idx + searchKey.length();
    int end   = body.indexOf('"', start);
    if (start <= 0 || end <= 0) return "";
    return body.substring(start, end);
}

// ─── Pushover Notification ────────────────────────────────────────────────────
void sendPushoverNotification(const String& title, const String& message) {
    if (strlen(PUSHOVER_TOKEN) < 10 || strlen(PUSHOVER_USER) < 10) {
        Serial.println("[Pushover] Credentials not set — notification skipped.");
        return;
    }
    Serial.println("[Pushover] Sending: " + title);

    WiFiClientSecure client;
    client.setInsecure();
    HTTPClient https;

    if (https.begin(client, "https://api.pushover.net/1/messages.json")) {
        https.addHeader("Content-Type", "application/json");

        String body = "{\"token\":\"" + String(PUSHOVER_TOKEN) + "\"," +
                       "\"user\":\""  + String(PUSHOVER_USER)  + "\"," +
                       "\"title\":\"" + title                  + "\"," +
                       "\"message\":\"" + message              + "\"," +
                       "\"sound\":\"magic\"}";

        int code = https.POST(body);
        if (code == 200) {
            Serial.println("[Pushover] Notification sent successfully.");
        } else {
            Serial.printf("[Pushover] HTTP %d — %s\n", code, https.getString().c_str());
        }
        https.end();
    } else {
        Serial.println("[Pushover] Failed to connect.");
    }
}

