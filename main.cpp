// ─── Pill Detection ───────────────────────────────────────────────────────────
// Monitors the IR sensor for up to windowMs after dispensing.
// Logs detection start, end, and duration to Serial.
void detectPill(unsigned long windowMs = 8000) {
    Serial.println("[IR] Waiting for pill detection...");
    unsigned long start     = millis();
    unsigned long detectAt  = 0;
    unsigned long releaseAt = 0;
    bool detected = false;

    while (millis() - start < windowMs) {
        bool pillPresent = (digitalRead(IR_SENSOR_PIN) == LOW);

        if (pillPresent && !detected) {
            detected  = true;
            detectAt  = millis();
            Serial.println("[IR] Pill detected!");
        }
        if (!pillPresent && detected && releaseAt == 0) {
            releaseAt = millis();
            unsigned long duration = releaseAt - detectAt;
            Serial.printf("[IR] Pill passed. Duration in sensor: %lu ms\n", duration);
            break; // done — pill passed through
        }
        delay(10);
    }

    if (!detected) {
        Serial.println("[IR] No pill detected within window — possible empty slot.");
    }
}



void runDispenseSequence() {
    Serial.println(">>> Step 1: Rotating Slot...");
    stepper.move(18);
    while (stepper.distanceToGo() != 0) {
        stepper.run();
        server.handleClient();
    }
    Serial.println(">>> Step 2: Waiting 6 seconds...");
    delay(6000);
    Serial.println(">>> Step 3: Pumping liquid (6s)...");
    digitalWrite(PUMP_PIN, HIGH);
    delay(40000); // run pump for 10 seconds to ensure pill is fully dispensed
    digitalWrite(PUMP_PIN, LOW);
    Serial.println(">>> Sequence Complete.");
}

