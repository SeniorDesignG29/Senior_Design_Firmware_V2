void handleTakeNow() {
    server.sendHeader("Access-Control-Allow-Origin", "*");
    server.send(200, "application/json", "{\"status\":\"success\"}");
    runDispenseSequence();
    detectPill(); // log pill detection after manual dispense
}

void handleUpdate() {
    server.sendHeader("Access-Control-Allow-Origin", "*");
    if (!server.hasArg("plain")) {
        server.send(400, "application/json", "{\"error\":\"No body\"}");
        return;
    }
    String body = server.arg("plain");

    // Respond immediately so app doesn't time out
    server.send(200, "application/json", "{\"status\":\"success\"}");

    String newUsername = extractJsonString(body, "username");
    String newMedName  = extractJsonString(body, "medName");
    String newTime     = extractJsonString(body, "time");
    String currentTime = extractJsonString(body, "currentTime");

    if (newUsername.length() > 0) userName = newUsername;
    if (newMedName.length()  > 0) medName  = newMedName;

    if (newTime.length() >= 5) {
        medTime      = newTime;
        targetHour   = newTime.substring(0, 2).toInt();
        targetMinute = newTime.substring(3, 5).toInt();
        timerActive  = true;
        Serial.println("=== UPDATE RECEIVED ===");
        Serial.printf("  userName:    %s\n", userName.c_str());
        Serial.printf("  medName:     %s\n", medName.c_str());
        Serial.printf("  rawTime:     %s\n", newTime.c_str());
        Serial.printf("  targetHour:  %d\n", targetHour);
        Serial.printf("  targetMin:   %d\n", targetMinute);
        Serial.printf("  timerActive: YES\n");
        Serial.println("=======================");
    }

    if (currentTime.length() >= 19) {
        rtc.adjust(DateTime(
            currentTime.substring(0,  4).toInt(),
            currentTime.substring(5,  7).toInt(),
            currentTime.substring(8,  10).toInt(),
            currentTime.substring(11, 13).toInt(),
            currentTime.substring(14, 16).toInt(),
            currentTime.substring(17, 19).toInt()
        ));
        Serial.println("RTC synced: " + currentTime);
    }

    updateDisplay();
}

